    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        String errorTitle = "Ooops";
        try{
        String firstname;
        String lastname;
        int age;
        String gender;
        String phone;
        String continent;
        String hobbies;
        String email;
        
        
        
        
        
        // Extract and store user's data from components
        firstname = firstnameTextField.getText();
//        age = Integer.valueOf(ageTextField.getText());
//        college = collegeButtonGroup.getSelection().getActionCommand();
        lastname = lastnameTextField.getText();
        email = emailTextField.getText();
        
        //check name valid
        if (firstname.isBlank()){
           JOptionPane.showMessageDialog(rootPane, "Invalid first name provided",errorTitle, JOptionPane.ERROR_MESSAGE);
           return;
        }
        
        if (lastname.isBlank()){
           JOptionPane.showMessageDialog(rootPane, "Invalid last name provided",errorTitle, JOptionPane.ERROR_MESSAGE);
           return;
        }
       
        
        age = (int) this.ageSpinner.getModel().getValue();
        gender = collegeButtonGroup.getSelection().getActionCommand();
        continent = (String) this.continentComboBox.getModel().getSelectedItem();
        hobbies = this.hobbiesTextArea.getText();
        phone = this.phoneFormattedTextField.getText();
        
        
        //check age valid
        if (age < 18){
            JOptionPane.showMessageDialog(rootPane, "Invalid age provided",errorTitle, JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(gender.equals("Male")){
            gender = "Male";
        }
        
        if(gender.equals("Female")){
            gender = "Female";
        }
        
        if (email.isBlank()){
           JOptionPane.showMessageDialog(rootPane, "Invalid email provided",errorTitle, JOptionPane.ERROR_MESSAGE);
           return;
        }
        
        
        
        if(phone.replace("-","").isBlank()){
            JOptionPane.showMessageDialog(rootPane, "Invalid phone number provided", errorTitle, JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (continent == null){
            JOptionPane.showMessageDialog(rootPane, "Invalid continent provided", errorTitle, JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (hobbies.length() <= 5 || hobbies.isBlank()){
            JOptionPane.showMessageDialog(rootPane, "Invalid hobbies provided", errorTitle, JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        User user;
        //create a variable for success message
        user = new User(firstname, lastname, age, gender, email, phone, continent, hobbies, this.photo);
        
        
        JOptionPane.showMessageDialog(rootPane, user, "Success!", JOptionPane.INFORMATION_MESSAGE, new ImageIcon(user.getPhoto()));
        
        
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Invalid age provided", errorTitle, JOptionPane.ERROR_MESSAGE);
 //           System.out.println("Error: invalid age provided");
        } catch (NullPointerException e){
            JOptionPane.showMessageDialog(rootPane, "No gender selected", errorTitle, JOptionPane.ERROR_MESSAGE);
 //           System.out.println("Error: No Gender selected");
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, "Something went wrong!", errorTitle, JOptionPane.ERROR_MESSAGE);

  //          System.out.println("Error" + e.getMessage());
           
        }
        
    }//GEN-LAST:event_submitButtonActionPerformed

