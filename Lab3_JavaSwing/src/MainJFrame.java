    private void photoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_photoButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
    
    FileNameExtensionFilter filter = new FileNameExtensionFilter("Image files", "jpg", "png", "jpeg", "gif");
    file.setFileFilter(filter);
    
    if (file.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
        try {
            BufferedImage img = ImageIO.read(file.getSelectedFile());
            Image edited_image = img.getScaledInstance(60, 80,
                                    Image.SCALE_SMOOTH);
            if (edited_image != null) {
                this.photoTextField.setText(file.getSelectedFile().getAbsolutePath());
                this.photo = edited_image;
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Please upload image correctly.", "Error - Incorrect image", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
    }
}
    }//GEN-LAST:event_photoButtonActionPerformed

   
   

